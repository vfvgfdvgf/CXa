def build_quote_message(city_name=None, service_name=None):
    pieces = ["مرحبًا، أريد عرض سعر"]
    if service_name:
        pieces.append(f"لخدمة {service_name}")
    if city_name:
        pieces.append(f"في {city_name}")
    return quote(" ".join(pieces))
